export function getLevel(levelId) {
  return CAMPAIGN_LEVELS.find((level) => level.id === Number(levelId)) || CAMPAIGN_LEVELS[0];
}

export function getEnvironment(environmentId) {
  return ENVIRONMENTS[environmentId] || ENVIRONMENTS.runway;
}

export function environmentList() {
  return Object.values(ENVIRONMENTS);
}

export function levelStars(level, result) {
  if (!result?.completed) return 0;
  let stars = 1;
  const coinRatio = result.totalCoins ? result.coinsCollected / result.totalCoins : 0;
  if (coinRatio >= (level.id >= 7 ? 0.75 : 0.6)) stars = 2;
  if (result.challengeComplete || (coinRatio >= 0.8 && result.fuelLeft >= 25)) stars = 3;
  return stars;
}
