export const METRES_PER_WORLD_UNIT = 0.08;

export function worldUnitsToMetres(worldUnits) {
  return Math.max(0, Math.floor((worldUnits || 0) * METRES_PER_WORLD_UNIT));
}

const ENVIRONMENT_AVERAGE_SPEED = {
  runway: 320,
  canyon: 292,
  frost: 258,
  desert: 284,
  storm: 266,
  moon: 252,
  islands: 276,
  volcano: 262,
};

const TERRAIN_MULTIPLIER = {
  easy: 1.05,
  medium: 1.12,
  hard: 1.2,
  expert: 1.3,
};

function levelDistance({ environment, targetDurationSeconds, terrainDifficulty, majorSections }) {
  const speed = ENVIRONMENT_AVERAGE_SPEED[environment] || 290;
  const multiplier = TERRAIN_MULTIPLIER[terrainDifficulty] || 1.12;
  const worldUnits = speed * targetDurationSeconds * multiplier + majorSections * 140;
  return Math.ceil(worldUnits / 1000) * 1000;
}

function checkpointsFor(distance, count) {
  return Array.from({ length: count }, (_, index) => Math.round(distance * ((index + 1) / (count + 1)) / 100) * 100);
}

function campaignLevel(config) {
  const distance = levelDistance(config);
  return {
    ...config,
    expectedAverageSpeed: ENVIRONMENT_AVERAGE_SPEED[config.environment] || 290,
    distance,
    checkpoints: checkpointsFor(distance, config.checkpointCount),
  };
}

